                var result = response.data;
                if(result.code == 200) {
                    self.blogData = result.data.beans;
                } else {
                    self.$Message.error('加载列表失败' + result.message);
                }
                self.blogLoading = false;
            }).catch(function (error) {
                console.log(error);
                self.blogLoading = false;
